package week1.day2;

public class LearnLooping {
	
public static void main(String[] args) {
	//int i;
	for(int i=0;i<=10;i++)
		//Body of the Statement
	{
		System.out.println(i);
	}
	for (int i=5;i>=0;i--) {
		System.out.println(i);
	}
	for (int i=0; i<=5;i++)
	{
		System.out.println("Charu");
	}
	int a=1;
	while(a<=6) {
		System.out.println("Hi");
        a++;
	}
}
}
